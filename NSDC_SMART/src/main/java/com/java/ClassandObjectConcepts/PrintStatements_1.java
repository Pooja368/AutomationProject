
package com.java.ClassandObjectConcepts;

public class PrintStatements_1 {

	public static void main(String[] args) {
		//Printing type of the literals
     System.out.println("I Love India!!");
     System.out.println(90);
     System.out.println(90.9);
     System.out.println('P');
     System.out.println(true);
     // printing based on the formula
     System.out.println(90+2);
     System.out.println(5-3);
     System.out.println(3*4);
     System.out.println(90/5);
     System.out.println(90/2);
     //Concatenation
     System.out.println("I Love India!!"+"I Love Mom");
     System.out.println("Hello"+10);
     System.out.println(10+19+"we");
     System.out.println("you"+(23+34));
     System.out.println("Hi"+'P');
     

	}

}
